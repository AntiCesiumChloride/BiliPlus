<div class="framesubtitle">BiliPlus能够为我做什么？</div>
<div class="framedescription">BiliPlus可以使您：<br/>·方便地下载哔哩哔哩上的任意视频/弹幕文件<br/>·选择您喜欢的弹幕播放器播放弹幕视频<br/>·通过简洁的页面无干扰地欣赏弹幕视频<br/>·快速搜索哔哩哔哩上的投稿/专题并直接下载/播放</div>
<br/>
<hr>
<div class="framesubtitle">BiliPlus是怎样实现这些功能/获得这些数据的？</div>
<div class="framedescription">目前，BiliPlus中所有数据通过以下接口获得：<br/>·哔哩哔哩弹幕网开放平台(api.bilibili.tv) [请参阅：<a href="http://docs.bilibili.cn/wiki/%E5%88%86%E7%B1%BB:API" target="_blank">分类:API - 嗶哩嗶哩百科</a>]<br/>·哔哩哔哩弹幕网内部接口(interface.bilibili.tv) [请与 <a href="mailto:orz@loli.my" target="_blank">orz@loli.my</a> 联系]<br/>·吐槽弹幕网开放平台(www.tucao.cc/api_v2) [请参阅：<a href="http://www.tucao.cc/api_v2/api.txt" target="_blank">API_V2参数使用格式说明</a>]<br/>·哔哩哔哩唧唧ASS弹幕接口(非开放接口) [本接口为非开放接口，仅供哔哩哔哩唧唧及BiliPlus内部使用]<br/>·BiliSound MP3下载接口(www.bilibili.fm) [站点升级中，请等待新版本API]</div>
<br/>
<hr>
<div class="framesubtitle">为什么我总是遇到错误？</div>
<div class="framedescription">由于BiliPlus通过哔哩哔哩开放平台实时获取信息，因此BiliPlus的稳定性依赖于哔哩哔哩开放平台，我们将不断改进程序以为您提供更好的服务。<br/>以下是对于一些常见错误代码的解释，您可以参照这些解释明白错误原因。<br/><b>[-1] app key error</b> AppKey错误。请直接刷新数据。<br/><b>[-404] no such doc</b> 文档不存在。请确定您输入了正确的AV号。<br/><b>[-503] overspeed</b> API请求过于频繁导致B站拒绝响应。可能BiliPlus目前访客过多，请稍后刷新页面即可。<br/>如遇到其他错误欢迎与BiliPlus联系。</div>
